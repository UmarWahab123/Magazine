      <p class="bold">Eradicating bias: a continuous journey</p>
      <p>“[Inclusive design] is particularly important in healthcare because typically excluded people are likely to make up a much higher proportion of the total user population within the healthcare space when compared with other industries,” says Becky Ferraro, Inclusive Design Lead at Deloitte. “It is becoming increasingly documented that there are patterns of exclusion that occur within healthcare and this can be the difference between life and death. For example, pulse oximetry devices used for warning of low blood oxygenation in covid-19 and other diseases may be missing three times as many cases of hypoxaemia in black patients as in white patients.* (*From a study report published in the New England Journal of Medicine.)</p>
    </div>
    <div class="col-md-4 mt-4">
      <p>We know that bias is the enemy to an inclusive AI. But it can be challenging to get this right. As with all approaches to embedding inclusion into design processes, eradicating bias is a continuous journey rather than a one-time act.”</p>
    <p>If there is one thing to keep in mind, it’s that AI will reflect the people who build it, so we need to ensure diversity in its creation, with a diverse team that’s always paired with a collaborative and open design process.</p>
      <p>“This is an all-too-common pitfall, where people choose to design behind closed doors through fear of rejection, failure or loss of ownership,” says Becky. “In reality, these fears have a much smaller negative impact than reputational damage or harm to others because of an AI that showed bias and wasn’t inclusive.”</p>
      <p> The other lens to collaboration in the AI development process is the idea of trust, given that the people that we need to input into algorithms and design process are perhaps also the people who trust us the least, who may be the least willing to be involved. </p>
      <p> “Through the stories of AI going wrong and the fear of the unknown, many people may be hesitant to get involved,” Becky adds. “I think part of this comes down to thinking of AI as something already built and out of the box; something that is less controlled. However, if we work on flipping the narrative to talk about the limited nature of AI at the start, and the ability to train and continuously shape its output, it will help people understand the value their input can have on creating a more trustworthy intelligence.”</p>

