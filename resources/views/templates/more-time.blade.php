          <p class="bold">Key trends in the inclusive healthcare experience</p>
          <p>Digital inclusion and assisted digital strategies are increasingly important in healthcare. Digital provides an opportunity to connect with more people in multiple environments and allows people to receive care and advice within their own space. However, it’s important to remember that not everyone has the access, skills, or ability to engage with digital. </p>
        <p>Therefore, there are two approaches that need to be considered. The first is an omnichannel approach, meaning people can interact with a product or service in several ways that best suit them. This could be digital, physical or telephony. Importantly, we need to ensure people can move between channels as circumstances change, and we need to design for flexibility. The other benefit of designing across channels is, of course, reliability, which is, again, increasingly critical in healthcare when we are talking about quality of life and risk of harm. If your digital channel breaks, what is the back-up? How can people continue to access the care that they need? </p>
        </div>
        <!--  -->
        <div class="col-md-4 mt-4">
          <p>The second point is about providing an opportunity for people to upskill. Just because someone struggles to use digital products, it doesn’t mean they don’t want to, especially when the ability to interact with them could drastically increase their quality of life and the communication and care available to them. This doesn’t have to be something you include in the direct design, but it can be something you can easily signpost to, or allow a customer to request. </p>
        <p>Co-design is a recognised and utilised tool being applied to many healthcare products and services. People are increasingly aware of the need to include the customer in the design process.</p>
        <p>However, many people default to using the ‘co-design’ label on what is, in fact, a focus group centred around the problem. The benefit of co-design actually comes from including customers in the solution as well as listening to their needs and requirements. The simple switch people can make is using tools from the design process in ideation activity sessions and challenging themselves on whether they are solely relying on asking about the experience rather than acting on the experience.</p>
        </div>
        <!--  -->
        <div class="col-md-4 mt-4">
          <p>It’s important to design for and with the entire care ecosystem; the patient, care providers, healthcare professionals, family and friends. Without taking this approach you can fail to identify barriers that could take away from the benefits of a product or service.</p>
          <p>“Now is the time to bring inclusive design and designers to the table,” agrees Dr Melanie Flory. “To be truly human-centred means the broadest range of diverse stakeholders, from clinician and patient to carers and other support networks, must be included in informing and shaping design ideas and processes from ideation to innovation. Integrating frontline users, and their input and feedback, throughout the design process is vital to successful human-centric AI innovation and assisted care.</p>
