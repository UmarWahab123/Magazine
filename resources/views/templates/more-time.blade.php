      <h3 class="con-intro-h1">When is a bed not a bed? </h3>
      <h3 class="more-time-h">Case study: Kettering General. Source: NHS England </h3>
      <div class="row">
        <div class="col-md-4 mt-4">
          <p>NHS AI lab Skunkworks helped Kettering General Hospital explore AI to improve bed management and generate options for moving patients in a way that would support the human team to make the best decisions, and achieve the goal of ‘right patient, right bed, right care, right time’. </p>

          <p>  Managed by a human team relying on individual expertise to deliver an air traffic control-esque system – deciding the best arrangement with a changing set of demands and patient numbers – bed scheduling is complex. No two patients are the same and staff are overwhelmed with options. </p>
            <p>Patient admissions were likened to a game of Tetris, where the allocation of each bed can have a major knock-on effect to the smooth-running of admissions and patient welfare. </p>
          </div>
          <div class="col-md-4 mt-4">
            <p>The team provided a proof-of-concept tool using historic data to create a virtual hospital predicting demand and making bed allocation suggestions to support better, faster decision-making. A Bayesian modelling approach was used to estimate how many patients with specific characteristics would present at the hospital over time, and compare different bed allocation approaches. Assigning beds based on the best bed available at the point of admission, the quick, less resource-intensive ‘greedy allocation’ method was implemented. </p>
         <p>Showing current occupancy rates, forecasting bed demand and explaining bed suggestions, the user interface enabled staff to test the model on a range of patients with different attributes and constraints to validate its performance. They now hope to develop and operationalise the tool, building connections to patient data in real-time, refining the algorithm, and understanding how it can be integrated into real-world site management.</p>
