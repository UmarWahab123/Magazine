            <p class="bg-dark white ps-3 pe-3 pt-1 pb-1" style="width: 12%;">FOREWORD</p>
            <p>Evolving the built environment puts us at a unique intersection. We span technology, economics, local and global regulation, environmentalism, and the health and wellbeing of society. We craft the stage where lives – billions of them – play out every day.</p>
            <p>The privilege, magnitude, complexity, and responsibility of this role can sometimes feel daunting. With every innovation, every development in how we work, and each impactful project, the need for more, better, newer seems to follow. The world feels fast, vast, and often out of control. Despite the pioneering developments they may feature, when projects take years to come to fruition, it can feel as though there’s always more that could be done.</p>
            <p>So how do we combat that overwhelming feeling? How do we even begin to make changes that keep pace?</p>
            <h1 class="con-intro-h1">We explore.</h1>
            <p>...Whether it’s working out how we climb our own Everest, and the ways in which contrasting characters can fruitfully collaborate in the name of one goal, or roadtesting a pragmatic approach to a notoriously knotty global challenge. Maybe it’s diving headfirst into the applications of machine learning and how they might free up more of our time and help us, ironically, to be more human in society’s most crucial settings.</p>
            <p class="bold" style="font-size: 30px;">By being curious, and embracing risk, we build agility, adaptability and confidence in the face of adversity.</p>
            <p>In this, our 10th issue, we invite you to join us in pausing for reflection before we continue tackling the often daunting tasks ahead. We ask you to take a moment to look back at how far we’ve come – with the help of bold and pioneering minds from many a discipline – in order to understand where we are going, how we might get there, and what that will require. Then, to work: navigating new worlds in design as we power forward towards our common destination of a viable, sustainable, human-centric, planet-conscious future.</p>
            <p>Eat. Sleep. Explore. Repeat...</p>
